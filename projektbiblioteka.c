float odejmowanie(int liczba1, int liczba2) {
	return (float)(liczba1 - liczba2);
}

float dzielenie(int liczba1, int liczba2) {
	return (float)(liczba1/((float)liczba2));
}

float dodawanie(int liczba1, int liczba2) {
	return (float)(liczba1+liczba2);
}

float mnozenie(int liczba1, int liczba2) {
	return (float)(liczba1*liczba2);
}
